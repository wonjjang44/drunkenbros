package com.drunkenbros.model.domain;

public class MemberSearchKeyWord {
	private String search_Mode;
	private String search_Word;
	private String search_Auth;
	
	public String getSearch_Mode() {
		return search_Mode;
	}
	public void setSearch_Mode(String search_Mode) {
		this.search_Mode = search_Mode;
	}
	public String getSearch_Word() {
		return search_Word;
	}
	public void setSearch_Word(String search_Word) {
		this.search_Word = search_Word;
	}
	public String getSearch_Auth() {
		return search_Auth;
	}
	public void setSearch_Auth(String search_Auth) {
		this.search_Auth = search_Auth;
	}
}
